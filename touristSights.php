                <div class="col-lg-6 text-center p-3">
                    <h2>HRÁDEK - CZECH MUSEUM OF SILVER</h2>
                    <p class="lead">Hrádek (small castle) was first mentioned in 1312, when it was still a fortified timber
                        stronghold. In the years 1400—1420, the Kutná Hora royal magistrate Václav of Donn converted the
                        fortification into a miniature castle – a High Gothic mansion. The house changed hands frequently,
                        and Hrádek was devastated during the Hussite Wars. Jan Smek of Vrchovit, a mine owner and ore
                        merchant, expanded Hrádek into a patrician mansion in the Jagiellonian Gothic style with spacious
                        halls, bay windows, and a chapel in the late 15th century.</p>
                    <p>The Czech Museum of Silver is the heir of the Vocel Archaeological Society, which was established in
                        1877. Today, the museum is one of the oldest and wealthiest museums in the Czech Republic, with a
                        collection of around 185,000 pieces.</p>
