                    <h2>STONE HOUSE</h2>
                    <p class="lead">The Stone House was erected on pre-Hussite foundations and is now considered one of
                        Europe's most important monuments of Late Gothic architecture. It is also part of a UNESCO World
                        Heritage-listed urban conservation area.</p>
                    <p>Prokop Kroupa, a burgher who was raised to nobility in 1499, owned the house. The knights on the
                        gable attested to this. Wroclaw's master Briccius Gauske created the house's highly adorned stone
                        gable. The Stone House is one of the structures managed by the Czech Museum of Silver, which was
                        established in 1877 as the successor to the Vocel Archaeological Society. The museum is now one of
                        the oldest and most valuable in the Czech Republic, with a collection of around 185,000 artifacts.
                    </p>
